const ValidateTextBox80 = (string) => {
	if(!string) {
		return true;
	}
	if(string.length > 80) {
		return false;
	}
	return true;
}

const ValidateEmail = (email) => {
	if (!email) {
		return false;
	}
	const reg = /^[\w.]+@[a-zA-Z_]+?\.[a-zA-Z]{2,3}$/;
	if (!email.match(reg)) {
		return false;
	}
	if(email.length > 30) {
		return false;
	}
	return true;
}

const ValidateContacto = (contacto) => {
	if(!contacto) {
		return true;
	}
	const reg = new RegExp(/^[\+]?[(]?[0-9]{3}[)]?[-\s\.]?[0-9]{3}[-\s\.]?[0-9]{4,6}$/im);
	if(!contacto.match(reg)) {
		return false;
	}
	return true;
}

const ValidateNombre = (nombre) => {
    if(!nombre) {
        return false;
    }

	if(nombre.length > 80) {
		return false;
	}
	if(nombre.length < 3) {
		return false;
	}
	return true;
}

const ValidateForm = () => {
	let nombre= document.getElementById("nombre").value;
	let email= document.getElementById("email").value;
	let contacto= document.getElementById("telefono").value; 
	let region= document.getElementById("region").value;
	let comuna= document.getElementById("comuna").value;
	let textbox = document.getElementById("descripcion").value;
	let transporte = document.getElementById("transporte").value;
	let sports = document.getElementById("sports");
	let invalidInputs = [];
	let isValid = true;

    const setInvalidInput = (inputName) => {
        invalidInputs.push(inputName);
        isValid &&= false;
    };

	if(!ValidateNombre(nombre)) {
		setInvalidInput("Nombre");
	}
	if(!ValidateEmail(email)) {
		setInvalidInput("Email");
	}
	if(!ValidateContacto(contacto)) {
		setInvalidInput("Telefono");
	}
	if(!ValidateSelect(region)) {
		setInvalidInput("Region");
	}
	if(!ValidateSelect(comuna)) {
		setInvalidInput("Comuna");
	}
	if(!ValidateTextBox80(textbox)) {
		setInvalidInput("Descripcion");
	}
	if(!ValidateSelect(transporte)) {
		setInvalidInput("Transporte");
	}
    if(!ValidateSports(sports)) {
        setInvalidInput("Los deportes deben ser entre 1 y 3");
    }
    // finally display validation
    let validationBox = document.getElementById("val-box");
    let validationMessageElem = document.getElementById("val-msg");
    let validationListElem = document.getElementById("val-list");

    if (!isValid) {
        validationListElem.textContent = "";
        // add invalid elements to val-list element.
        for (input of invalidInputs) {
        let listElement = document.createElement("li");
        listElement.innerText = input;
        validationListElem.append(listElement);
        }
        // set val-msg
        validationMessageElem.innerText = "Los siguiente campos son invalidos:";

        // make validation prompt visible
        validationBox.hidden = false;
    } else {
        document.getElementById("confirm-box").hidden = false;
        document.getElementById("form-group").hidden = true;
        let confirm_button = document.getElementById("confirm-button");
        let deny_button = document.getElementById("deny-button");
        confirm_button.addEventListener("click", () => {
            document.getElementById("form-group").hidden = true;
            document.getElementById("confirm-box").hidden = true;
            document.getElementById("return-box").hidden = false;
            document.getElementById("return-box").addEventListener("click", () => {window.location.href = "../index.html"})
        });
        deny_button.addEventListener("click", () => {
            document.getElementById("confirm-box").hidden = true;
            document.getElementById("form-group").hidden = false;
        });
    }
}
document.getElementById("region").addEventListener("change", updateComunas);
